import fs from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const repoRoot = path.resolve(__dirname, '..');

const cardsSourcePath = path.join(repoRoot, 'src', 'data', 'cards.ts');
const outputDir = path.join(repoRoot, 'public', 'cards');
const localMapPath = path.join(repoRoot, 'src', 'data', 'localCardImages.ts');
const timeoutMs = 30000;

const cardWidth = 400;
const cardHeight = 300;
const imageModel = process.env.POLLINATIONS_IMAGE_MODEL || 'flux';
const imageBaseUrl = process.env.POLLINATIONS_IMAGE_BASE || 'https://gen.pollinations.ai/image';
const pollinationsApiKey = process.env.POLLINATIONS_API_KEY || '';

const cardBackPrompt = 'ornate fantasy trading card back design, glowing runes, dark siberian arcane style, symmetrical composition';
const cardBackSeed = 9001;

const userAgent = 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/122.0.0.0 Safari/537.36';
const supportedImageExt = new Set(['png', 'webp', 'jpg', 'jpeg', 'gif']);

function buildPollinationsUrl(prompt, seed) {
  const fullPrompt = `fantasy card game art, ${prompt}, dark fantasy style, digital painting`;
  const encodedPrompt = encodeURIComponent(fullPrompt);
  const query = new URLSearchParams({
    model: imageModel,
    width: String(cardWidth),
    height: String(cardHeight),
    seed: String(seed),
    nologo: 'true',
  });
  if (pollinationsApiKey) {
    query.set('key', pollinationsApiKey);
  }
  return `${imageBaseUrl}/${encodedPrompt}?${query.toString()}`;
}

function extFromContentType(contentType) {
  const value = (contentType || '').split(';')[0].trim().toLowerCase();
  if (value === 'image/png') return 'png';
  if (value === 'image/webp') return 'webp';
  if (value === 'image/jpeg' || value === 'image/jpg') return 'jpg';
  if (value === 'image/gif') return 'gif';
  return 'jpg';
}

async function fetchImage(url) {
  const controller = new AbortController();
  const timer = setTimeout(() => controller.abort(), timeoutMs);
  try {
    const headers = {
      'User-Agent': userAgent,
    };
    if (pollinationsApiKey) {
      headers.Authorization = `Bearer ${pollinationsApiKey}`;
    }

    const res = await fetch(url, {
      signal: controller.signal,
      headers,
    });
    if (!res.ok) {
      const body = await res.text().catch(() => '');
      throw new Error(`HTTP ${res.status}${body ? `: ${body.slice(0, 120)}` : ''}`);
    }
    const bytes = new Uint8Array(await res.arrayBuffer());
    return {
      contentType: res.headers.get('content-type') || '',
      bytes,
    };
  } finally {
    clearTimeout(timer);
  }
}

function parseCardsForImages(cardsSource) {
  const cards = [];
  const entryRegex = /id:\s*'([^']+)'[\s\S]*?imageUrl:\s*img\('([^']+)',\s*(\d+)\)/g;

  let match;
  while ((match = entryRegex.exec(cardsSource)) !== null) {
    cards.push({
      id: match[1],
      prompt: match[2],
      seed: Number(match[3]),
    });
  }

  return cards;
}

function buildLocalMapModule(cardMap, cardBackPath) {
  const lines = [];
  lines.push('// Auto-generated by scripts/cache-card-images.mjs');
  lines.push('// Do not edit manually.');
  lines.push('');
  lines.push('export const LOCAL_CARD_IMAGES: Record<string, string> = {');
  const entries = Object.entries(cardMap).sort((a, b) => a[0].localeCompare(b[0]));
  for (const [cardId, localPath] of entries) {
    lines.push(`  '${cardId}': '${localPath}',`);
  }
  lines.push('};');
  lines.push('');
  lines.push(`export const LOCAL_CARD_BACK_IMAGE = '${cardBackPath || ''}';`);
  lines.push('');
  return lines.join('\n');
}

async function readExistingImageFiles(dirPath) {
  const result = new Map();
  let entries = [];
  try {
    entries = await fs.readdir(dirPath, { withFileTypes: true });
  } catch {
    return result;
  }

  for (const entry of entries) {
    if (!entry.isFile()) continue;
    const dotIndex = entry.name.lastIndexOf('.');
    if (dotIndex <= 0) continue;
    const base = entry.name.slice(0, dotIndex);
    const ext = entry.name.slice(dotIndex + 1).toLowerCase();
    if (!supportedImageExt.has(ext)) continue;
    result.set(base, entry.name);
  }
  return result;
}

async function writeLocalMapFile(cardMap, cardBackPath) {
  const moduleContent = buildLocalMapModule(cardMap, cardBackPath);
  await fs.writeFile(localMapPath, moduleContent, 'utf8');
}

async function main() {
  if (!pollinationsApiKey) {
    console.log('WARN: POLLINATIONS_API_KEY is not set. Requests to gen.pollinations.ai may fail with 401.');
  }

  const cardsSource = await fs.readFile(cardsSourcePath, 'utf8');
  const cards = parseCardsForImages(cardsSource);
  if (cards.length === 0) {
    throw new Error('No cards with imageUrl: img(...) were found in src/data/cards.ts');
  }

  await fs.mkdir(outputDir, { recursive: true });
  const existingFiles = await readExistingImageFiles(outputDir);

  const localCardImages = {};
  const failed = [];

  console.log(`Found ${cards.length} card image entries.`);

  for (const [index, card] of cards.entries()) {
    const existing = existingFiles.get(card.id);
    if (existing) {
      localCardImages[card.id] = `/cards/${existing}`;
      console.log(`[${index + 1}/${cards.length}] SKIP ${card.id} -> ${existing}`);
      await writeLocalMapFile(localCardImages, '');
      continue;
    }

    const url = buildPollinationsUrl(card.prompt, card.seed);
    try {
      const { contentType, bytes } = await fetchImage(url);
      const ext = extFromContentType(contentType);
      const fileName = `${card.id}.${ext}`;
      const filePath = path.join(outputDir, fileName);
      await fs.writeFile(filePath, bytes);
      localCardImages[card.id] = `/cards/${fileName}`;
      existingFiles.set(card.id, fileName);
      console.log(`[${index + 1}/${cards.length}] OK  ${card.id} -> ${fileName}`);
      await writeLocalMapFile(localCardImages, '');
    } catch (error) {
      failed.push({ id: card.id, reason: String(error) });
      console.log(`[${index + 1}/${cards.length}] ERR ${card.id} -> ${error}`);
    }
  }

  let localCardBack = '';
  const existingBack = existingFiles.get('card-back');
  if (existingBack) {
    localCardBack = `/cards/${existingBack}`;
    console.log(`Card back SKIP -> ${existingBack}`);
  }

  try {
    if (!localCardBack) {
      const backUrl = buildPollinationsUrl(cardBackPrompt, cardBackSeed);
      const { contentType, bytes } = await fetchImage(backUrl);
      const ext = extFromContentType(contentType);
      const fileName = `card-back.${ext}`;
      await fs.writeFile(path.join(outputDir, fileName), bytes);
      localCardBack = `/cards/${fileName}`;
      existingFiles.set('card-back', fileName);
      console.log(`Card back OK -> ${fileName}`);
    }
  } catch (error) {
    console.log(`Card back ERR -> ${error}`);
  }

  await writeLocalMapFile(localCardImages, localCardBack);

  const okCount = Object.keys(localCardImages).length;
  console.log('');
  console.log(`Saved: ${okCount}/${cards.length} card covers`);
  console.log(`Map file: ${path.relative(repoRoot, localMapPath)}`);
  console.log(`Output: ${path.relative(repoRoot, outputDir)}`);

  if (failed.length > 0) {
    console.log('');
    console.log('Failed downloads:');
    for (const item of failed) {
      console.log(`- ${item.id}: ${item.reason}`);
    }
  }

  if (okCount === 0) {
    process.exitCode = 1;
  }
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
